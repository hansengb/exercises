public class Quadratic
{
    public static void main ( String[] args)
    {
        double x = 0.0;
        double value = 1;
        System.out.println("When x = " + x);
        value = ((3 * x * x) - (8 * x) + (4));
        System.out.println("The function = " + value);
        x = 2.0;
        value = ((3 * x * x) - (8 * x) + (4));
        System.out.println("When x = " + x);
        System.out.println("The function = " + value);
        x = 4.0;
        value = ((3 * x * x) - (8 * x) + (4));
        System.out.println("When x = " + x);
        System.out.println("The function = " + value);
    }
}